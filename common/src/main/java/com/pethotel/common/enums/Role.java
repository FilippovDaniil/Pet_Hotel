package com.pethotel.common.enums;

// enum — специальный тип Java: конечный набор именованных констант.
// Компилятор гарантирует, что за пределами этого перечисления других значений не существует.
// Хранится в БД как строка (EnumType.STRING) — читаемо и устойчиво к изменению порядка.
//
// Роли определены в common, потому что используются сразу в нескольких сервисах:
//   - customer-service: сохраняет роль при регистрации, проставляет в JWT
//   - api-gateway: читает роль из JWT и кладёт в X-User-Role
//   - booking-service, dining-service и др.: читают X-User-Role для бизнес-правил
public enum Role {
    CUSTOMER,   // обычный клиент: бронирование, буфет, оплата
    RECEPTION,  // ресепшн: заселение / выселение гостей
    ADMIN       // администратор: управление данными, поддержка, смена ролей
}
